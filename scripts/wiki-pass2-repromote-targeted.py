PAGE_INDEX_FILE = PROJECT_ROOT / "working" / "wiki-parsed" / "page-index.jsonl"
WIKI_PASS2_DIR = PROJECT_ROOT / "working" / "wiki-pass2"
GRAPH_NODES_DIR = PROJECT_ROOT / "graph" / "nodes"

# ---------------------------------------------------------------------------
# The 14 pages to re-promote.
# Each entry: (wiki_page_name, bucket_id, correct_type, original_wrong_type_dir)
# For Group B, original_wrong_type_dir is None (no old file to delete;
# we overwrite in place under the correct locations/ dir).
# ---------------------------------------------------------------------------
TARGETED_PAGES = [
    # Group A: Wars — mistyped as place.location, should be event.war
    ("Spice War",             "locations-other-m-s",   "event.war",              "locations"),
    ("Salt War",              "locations-other-m-s",   "event.war",              "locations"),
    ("Third Turtle War",      "locations-other-s-t",   "event.war",              "locations"),
    ("Second Turtle War",     "locations-other-m-s",   "event.war",              "locations"),
    ("War of Three Princes",  "locations-other-t-y",   "event.war",              "locations"),
    ("War on Dagger Lake",    "locations-other-t-y",   "event.war",              "locations"),
    ("War for the dawn",      "locations-other-t-y",   "event.war",              "locations"),
    # Group A: Factions — mistyped as place.location, should be organization.faction
    ("Second Sons",           "locations-other-m-s",   "organization.faction",   "locations"),
    ("Triarchy",              "locations-other-s-t",   "organization.faction",   "locations"),
    # Group A: Artifact — mistyped as place.location, should be object.artifact
    ("Iron Throne",           "houses-major-recovery", "object.artifact",        "locations"),
    # Group B: Locations with religion-field bleed — type correct, edges malformed
    ("Seven Kingdoms",        "locations-other-s-t",   "place.location",         None),
    ("Pentos",                "locations-other-g-m",   "place.location",         None),
    ("Lys",                   "locations-other-g-m",   "place.location",         None),
    ("Tyrosh",                "locations-other-s-t",   "place.location",         None),
]

