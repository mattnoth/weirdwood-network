def resolve_type_dir(entity_type: str) -> str | None:
    if entity_type in TYPE_DIR_MAP:
        return TYPE_DIR_MAP[entity_type]
    parent = entity_type.split(".")[0]
    if parent in TYPE_DIR_MAP:
        return TYPE_DIR_MAP[parent]
    return None


# ---------------------------------------------------------------------------
# Slug generation (matches wiki-pass2-emit-deterministic.py exactly)
# ---------------------------------------------------------------------------

def page_to_slug(page_name: str) -> str:
    slug = page_name.lower()
    slug = re.sub(r"['\",]", "", slug)
    slug = re.sub(r"[ _]+", "-", slug)
    slug = re.sub(r"[^a-z0-9-]", "-", slug)
    slug = re.sub(r"-+", "-", slug).strip("-")
    return slug


# ---------------------------------------------------------------------------
# Atomic write helper (mirrors wiki-pass2-promote.py)
# ---------------------------------------------------------------------------

def atomic_write(dest_path: Path, data: bytes) -> None:
    dest_path.parent.mkdir(parents=True, exist_ok=True)
    staging = dest_path.parent / f".staging-{dest_path.name}.tmp"
    try:
        staging.write_bytes(data)
        os.rename(staging, dest_path)
    except Exception:
        try:
            staging.unlink(missing_ok=True)
        except Exception:
            pass
        raise


# ---------------------------------------------------------------------------
# Skeleton renderer (mirrors wiki-pass2-emit-deterministic.py::render_node)
# ---------------------------------------------------------------------------

def wiki_url(page_name: str) -> str:
    import urllib.parse
    encoded = urllib.parse.quote(page_name.replace(" ", "_"), safe="/:@!$&'()*+,;=")
    return f"https://awoiaf.westeros.org/index.php/{encoded}"


def render_edge_line(rel: dict) -> str:
    edge_type = rel.get("edge_type", "UNKNOWN_EDGE")
    target = rel.get("target", "")
    field = rel.get("field", "")
    direction = rel.get("direction", "forward")
    qualifier = rel.get("qualifier", "")

    edge_label = f"{edge_type} (reverse)" if direction == "reverse" else edge_type
    line = f"- {edge_label}: {target} (track_b: {field})"
    if qualifier:
        line += f" [{qualifier}]"
    return line


def render_skeleton(
    page_name: str,
    slug: str,
    entity_type: str,
    aliases: list,
    confidence: str,
    bucket_id: str,
    relationships: list,
    infobox_found: bool,
) -> str:
    url = wiki_url(page_name)

    if aliases:
        alias_items = ", ".join(f'"{a}"' for a in aliases)
        aliases_yaml = f"[{alias_items}]"
    else:
        aliases_yaml = "[]"

    lines = [
        "---",
        f'name: "{page_name}"',
        f"type: {entity_type}",
        f"slug: {slug}",
        f"aliases: {aliases_yaml}",
        f"confidence: {confidence}",
        f'wiki_source: "{url}"',
        f"bucket_id: {bucket_id}",
        "prompt_version: v1-python",
        "node_version: 1",
        "pass_origin: pass2-wiki-deterministic",
        "---",
        "",
        "## Identity",
        "",
        f"{page_name} is a {entity_type} from the AWOIAF wiki.",
        "",
        "## Edges",
        "",
    ]

    if relationships and infobox_found:
        for rel in relationships:
            lines.append(render_edge_line(rel))

    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--apply",
        action="store_true",
        default=False,
        help="Write new nodes and delete old wrong-type files. Without --apply, dry-run only.",
    )
    parser.add_argument(
        "-v", "--verbose",
        action="store_true",
        default=False,
        help="Print detailed per-page output.",
    )
    args = parser.parse_args()

    mode = "APPLY" if args.apply else "DRY-RUN"
    print(f"\n=== Targeted re-promotion of 14 parser-bug-affected nodes [{mode}] ===\n")

    # --- Load infobox-data.jsonl ---
    print(f"Loading {INFOBOX_DATA_FILE} ...")
    if not INFOBOX_DATA_FILE.exists():
        print(f"ERROR: {INFOBOX_DATA_FILE} not found. Re-run wiki-infobox-parser.py first.",
              file=sys.stderr)
        sys.exit(1)
    infobox_data: dict[str, dict] = {}
    with open(INFOBOX_DATA_FILE, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            infobox_data[rec["page"]] = rec
    print(f"  {len(infobox_data):,} infobox records loaded")

    # --- Load page-index.jsonl ---
    print(f"Loading {PAGE_INDEX_FILE} ...")
    if not PAGE_INDEX_FILE.exists():
        print(f"ERROR: {PAGE_INDEX_FILE} not found. Re-run wiki-infobox-parser.py first.",
              file=sys.stderr)
        sys.exit(1)
    page_index: dict[str, dict] = {}
    with open(PAGE_INDEX_FILE, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            page_index[rec["page"]] = rec
    print(f"  {len(page_index):,} page-index records loaded\n")

    # --- Process each targeted page ---
    results = []
    errors = []

    for page_name, bucket_id, expected_type, old_type_dir in TARGETED_PAGES:
        slug = page_to_slug(page_name)
        group = "B" if old_type_dir is None else "A"

        if args.verbose:
            print(f"  [{group}] {page_name!r} (slug={slug}, bucket={bucket_id})")

        # 1. Look up infobox data
        infobox_rec = infobox_data.get(page_name)
        page_idx_rec = page_index.get(page_name)

        if infobox_rec is None and page_idx_rec is None:
            msg = f"  WARNING: {page_name!r} not found in infobox-data.jsonl or page-index.jsonl — skipping"
            print(msg, file=sys.stderr)
            errors.append({"page": page_name, "error": "not in parser outputs"})
            continue

        # 2. Determine type — use the expected_type from our override table.
        #    This is already what the patched parser should produce (verified below),
        #    but we use the explicit expected_type as the authoritative value since
        #    this script is a targeted repair operation.
        actual_type = None
        if infobox_rec and infobox_rec.get("entity_type"):
            actual_type = infobox_rec["entity_type"]
        elif page_idx_rec and page_idx_rec.get("entity_type_guess"):
            actual_type = page_idx_rec["entity_type_guess"]

        if actual_type != expected_type:
            print(f"  WARNING: {page_name!r}: parser emits type={actual_type!r}, "
                  f"expected {expected_type!r}. Using expected_type. "
                  f"Check parser fix.", file=sys.stderr)
            errors.append({
                "page": page_name,
                "error": f"type mismatch: parser={actual_type}, expected={expected_type}",
            })
        entity_type = expected_type

        # 3. Aliases and relationships from infobox
        infobox_found = infobox_rec is not None
        aliases: list[str] = []
        relationships: list[dict] = []
        if infobox_rec:
            aliases = infobox_rec.get("aliases") or []
            relationships = infobox_rec.get("relationships") or []

        # 4. Determine confidence from the bucket manifest
        manifest_path = WIKI_PASS2_DIR / bucket_id / "manifest.json"
        confidence = "tier-1"  # default
        if manifest_path.exists():
            try:
                manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
                confidence = manifest.get("tier_default") or "tier-1"
            except (json.JSONDecodeError, OSError):
                pass

        # 5. Render new skeleton
        skeleton_content = render_skeleton(
            page_name=page_name,
            slug=slug,
            entity_type=entity_type,
            aliases=aliases,
            confidence=confidence,
            bucket_id=bucket_id,
            relationships=relationships,
            infobox_found=infobox_found,
        )
        skeleton_bytes = skeleton_content.encode("utf-8")

        # 6. Find existing prose from bucket artifact (if any)
        prose_path = WIKI_PASS2_DIR / bucket_id / "prose" / f"{slug}.prose.md"
        if prose_path.exists():
            prose_bytes = prose_path.read_bytes()
        else:
            prose_bytes = b""

        # 7. Concatenate
        if prose_bytes:
            final_bytes = skeleton_bytes + b"\n" + prose_bytes
        else:
            final_bytes = skeleton_bytes

        # 8. Resolve destination directory
        dest_subdir = resolve_type_dir(entity_type)
        if dest_subdir is None:
            print(f"  ERROR: unrecognised type {entity_type!r} for {page_name!r}",
                  file=sys.stderr)
            errors.append({"page": page_name, "error": f"unknown type dir for {entity_type}"})
            continue
        dest_dir = GRAPH_NODES_DIR / dest_subdir
        dest_path = dest_dir / f"{slug}.node.md"

        # 9. Old wrong-type file path (Group A only)
        old_path = None
        if old_type_dir is not None:
            old_path = GRAPH_NODES_DIR / old_type_dir / f"{slug}.node.md"

        # For Group B (in-place overwrite), check if destination already exists
        # with same content — skip if byte-equal (idempotent).
        outcome = "would_promote"
        if dest_path.exists():
            existing = dest_path.read_bytes()
            if existing == final_bytes:
                outcome = "byte_equal"
            else:
                outcome = "would_overwrite"
        else:
            outcome = "would_promote"

        if args.apply:
            atomic_write(dest_path, final_bytes)
            if outcome == "byte_equal":
                outcome = "skipped_byte_equal"
            elif outcome == "would_overwrite":
                outcome = "overwritten"
            else:
                outcome = "promoted"

            # Group A: delete old wrong-type file
            if old_path and old_path.exists():
                old_path.unlink()
                outcome += "+deleted_old"
        else:
            if old_path and old_path.exists():
                outcome += "+would_delete_old"

        results.append({
            "group": group,
            "page": page_name,
            "slug": slug,
            "entity_type": entity_type,
            "dest": str(dest_path),
            "old_path": str(old_path) if old_path else None,
            "has_prose": bool(prose_bytes),
            "edge_count": len(relationships),
            "infobox_found": infobox_found,
            "outcome": outcome,
        })

        if args.verbose:
            prose_tag = "skeleton+prose" if prose_bytes else "skeleton-only"
            print(f"    type={entity_type} | {len(relationships)} edges | {prose_tag}")
            print(f"    dest: {dest_path}")
            if old_path:
                print(f"    old:  {old_path}")
            print(f"    outcome: {outcome}")
            print()

    # --- Summary ---
    print()
    print("=" * 60)
    print(f"Pages attempted:   {len(TARGETED_PAGES)}")
    print(f"Errors/warnings:   {len(errors)}")
    print()
    print("Results:")
    for r in results:
        prose_tag = "prose" if r["has_prose"] else "skel-only"
        print(f"  [{r['group']}] {r['slug']:<35s} {r['entity_type']:<25s} "
              f"{r['edge_count']:>2} edges  {prose_tag:<10s}  {r['outcome']}")
    print()

    if errors:
        print("Errors / warnings:")
        for e in errors:
            print(f"  {e['page']}: {e['error']}")
        print()

    if not args.apply:
        print("Dry-run complete. Run with --apply to write files.")
    else:
        promoted = sum(1 for r in results if "promoted" in r["outcome"] or "overwritten" in r["outcome"])
        deleted = sum(1 for r in results if "deleted_old" in r["outcome"])
        skipped = sum(1 for r in results if "byte_equal" in r["outcome"])
        print(f"Files written/overwritten: {promoted}")
        print(f"Old wrong-type files deleted: {deleted}")
        print(f"Byte-equal (skipped): {skipped}")
    print("=" * 60)


if __name__ == "__main__":
    main()
