#!/usr/bin/env python3
"""Targeted re-promotion for 14 parser-bug-affected nodes.

Two groups of nodes were identified in the schema-drift audit (2026-04-30):

Group A (10 nodes): Entities mistyped as place.location in graph/nodes/locations/.
  - 7 wars -> event.war -> graph/nodes/events/
  - 2 factions -> organization.faction -> graph/nodes/factions/
  - 1 artifact -> object.artifact -> graph/nodes/artifacts/
  These old incorrect files in locations/ are deleted after re-promotion.

Group B (4 nodes): Location nodes with religion-field bleed (WORSHIPS: North etc.)
  in their ## Edges sections. Type is correct (place.location), but edges are
  malformed. These stay in locations/ but are overwritten with corrected output.

Approach (Option B — targeted rebuild, minimal blast radius):
  1. Re-reads the freshly-regenerated infobox-data.jsonl and page-index.jsonl
     (already patched — bugs fixed in wiki-infobox-parser.py).
  2. For each of the 14 slugs: re-emits skeleton via the same logic as
     wiki-pass2-emit-deterministic.py.
  3. Re-reads prose from the existing skeleton bucket artifact (not re-extracting
     from HTML — we use whatever prose was previously extracted and is in the
     bucket's prose/ directory, since prose extraction is not affected by the bugs).
  4. Concatenates skeleton + prose and atomic-renames to the correct target dir.
  5. For Group A: also deletes the old wrong-bucket file in locations/.

Invariants preserved:
  - Atomic-rename: never writes directly to graph/nodes/. Uses a staging temp file.
  - Only the 14 listed nodes change. No other graph/nodes/ files are touched.
  - Reads from working/wiki-parsed/ (freshly regenerated by parser re-run).
  - Does NOT modify working/wiki-pass2/ bucket manifests.

Usage:
  python3 scripts/wiki-pass2-repromote-targeted.py           # dry-run
  python3 scripts/wiki-pass2-repromote-targeted.py --apply   # write files
  python3 scripts/wiki-pass2-repromote-targeted.py -v        # verbose
"""

